    private void jButton2PedirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2PedirActionPerformed
        double devuelto;
        try {
            // TODO add your handling code here:
            Golosina g = maq.pedirGolosina(this.jTextField1Posicion.getText());
            if (g != null) {
                if (maq.getMonedero() == me.redondeoDosNumeros(g.getPrecio())) {
                    this.jTextField1Respuesta.setText("Se ha realizado la compra correctamente");
                } else if (maq.getMonedero() > g.getPrecio()) {
                    devuelto = maq.getMonedero() - g.getPrecio();
                    this.jTextField1Respuesta.setText("Compra realizada correctamente. Se te ha devuelto " + me.redondeoDosNumeros(devuelto) + "€");
                }
                if (g.getPosicion().equals("0A")) {
                    this.jLabelImagen.setIcon(new ImageIcon("./cerveza.jpg"));
                } else if (g.getPosicion().equals("6A")) {
                    this.jLabelImagen.setIcon(new ImageIcon("./aquarius.jpg"));
                } else if (g.getPosicion().equals("5A")) {
                    this.jLabelImagen.setIcon(new ImageIcon("./cocacola.jpg"));
                } else if (g.getPosicion().equals("5B")) {
                    this.jLabelImagen.setIcon(new ImageIcon("./fantalimon.jpeg"));
                } else if (g.getPosicion().equals("5C")) {
                    this.jLabelImagen.setIcon(new ImageIcon("./fantanaranja.jpg"));
                } else if (g.getPosicion().equals("5D")) {
                    this.jLabelImagen.setIcon(new ImageIcon("./nestea.jpg"));
                }
                maq.setMonedero(0);
                this.jTextFieldDineroInsertado.setText(null);
                this.jTextField1Posicion.setText(null);
                cargarDatos();
            }
        } catch (IncorrectPositionException ex) {
            this.jTextField1Respuesta.setText("Posición incorrecta, no existe esa posición");
            this.jLabelImagen.setIcon(null);
        } catch (EmptyPositionException ex) {
            this.jTextField1Respuesta.setText("No quedan. Pide otra cosa o solicita la devolución");
            this.jLabelImagen.setIcon(null);
        } catch (InsufficientMoney ex) {
            this.jTextField1Respuesta.setText("Dinero Insuficiente. Añade más dinero o solicita la devolución.");
            this.jLabelImagen.setIcon(null);
        } catch (SQLException ex) {
            this.jTextField1Respuesta.setText("Error al recuperar datos ");
            this.jLabelImagen.setIcon(null);
        } catch (Exception ex) {
            ex.printStackTrace();
            this.jTextField1Respuesta.setText("No se ha podido realizar la compra");
            this.jLabelImagen.setIcon(null);
        }
    }//GEN-LAST:event_jButton2PedirActionPerformed

