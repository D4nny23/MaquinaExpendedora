    public void cargarDatos() {
        DefaultTableModel m = (DefaultTableModel) this.jTableTiposGolosinas.getModel();
        m.setNumRows(0);
        try {
            ArrayList<Golosina> gs = maq.devuelveGolosina();
            for (Golosina g : gs) {
                String format = String.format("%.2f", g.getPrecio());
                Object[] o = {g.getNombre(), format, g.getCantidad(), g.getPosicion()};
                m.addRow(o);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            this.jTextField1Respuesta.setText("Se ha producido un error " + ex.getMessage());
        }
    }
